public class ArrayBasics {
    public static void main(String[] args) {
        int[] arr = {10, 20, 30, 40};
        for (int i = 0; i < arr.length; i++) {
            System.out.println("Value at position " + i + " is: " + arr[i]);
		
        }

	System.out.println("Array length: " + arr.length);
    }
}
